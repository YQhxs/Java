package three;

public class Point {
    private int point;
    private int x;
    private int y;

    public Point(int p, int x, int y) {
        this.point = p;
        this.x = x;
        this.y = y;
    }

    public int point() {
        return point;
    }

    public int X() {
        return x;
    }

    public int Y() {
        return y;
    }
}
